/**
 * @param {{
 *   unit: InventoryUnitRecord & { node: ts.Node; sourceFile: ts.SourceFile; };
 *   declarationUnitIdsBySymbol: Map<string, string[]>;
 * }} options
 */
function collectDependencySymbols(options) {
    const {
        unit,
        declarationUnitIdsBySymbol,
    } = options;
    const typeParameterNames = getTypeParameterNames(unit.node);
    const dependencySymbols = new Set();

    /**
     * @param {ts.Node} node
     */
    function visit(node) {
        if (ts.isTypeReferenceNode(node)) {
            addReference(node.typeName.getText(unit.sourceFile));
        }
        else if (ts.isExpressionWithTypeArguments(node)) {
            addReference(node.expression.getText(unit.sourceFile));
        }
        else if (ts.isTypeQueryNode(node)) {
            addReference(node.exprName.getText(unit.sourceFile));
        }
        else if (ts.isHeritageClause(node)) {
            for (const heritageType of node.types) {
                addReference(heritageType.expression.getText(unit.sourceFile));
            }
        }
        ts.forEachChild(node, visit);
    }

    visit(unit.node);

    for (const declaredSymbol of unit.declaredSymbols) {
        dependencySymbols.delete(declaredSymbol);
    }
    for (const typeParameterName of typeParameterNames) {
        dependencySymbols.delete(typeParameterName);
        for (const prefix of unit.containerPath.map((_, index) => unit.containerPath.slice(0, index + 1).join("."))) {
            dependencySymbols.delete(`${prefix}.${typeParameterName}`);
        }
    }

    return [...dependencySymbols]
        .filter(symbol => declarationUnitIdsBySymbol.has(symbol))
        .sort(compareStringsCaseSensitive);

    /**
     * @param {string} referenceText
     */
    function addReference(referenceText) {
        if (!referenceText || referenceText.includes("<") || typeParameterNames.has(referenceText)) {
            return;
        }
        const normalized = normalizeReference(referenceText, unit.containerPath, declarationUnitIdsBySymbol);
        if (normalized) {
            dependencySymbols.add(normalized);
        }
    }
}

/**
 * @param {Map<string, RootSurfaceRecord>} rootSurfaceByCompatName
 * @param {InventoryUnitRecord} declarationUnit
 */
function registerRootSurface(rootSurfaceByCompatName, declarationUnit) {
    if (declarationUnit.unitKind !== "declaration" || !declarationUnit.symbolName) {
        return;
    }

    let root = rootSurfaceByCompatName.get(declarationUnit.symbolName);
    if (!root) {
        root = {
            compatName: declarationUnit.symbolName,
            rootDeclarationUnitIds: [],
            instanceContainerSymbols: new Set(),
            staticContainerSymbols: new Set(),
        };
        rootSurfaceByCompatName.set(declarationUnit.symbolName, root);
    }

    switch (declarationUnit.declarationKind) {
        case "var":
            root.rootDeclarationUnitIds.push(declarationUnit.id);
            if (declarationUnit.typeRefName) {
                root.staticContainerSymbols.add(declarationUnit.typeRefName);
            }
            break;
        case "function":
        case "enum":
        case "type-alias":
        case "namespace":
            root.rootDeclarationUnitIds.push(declarationUnit.id);
            root.staticContainerSymbols.add(declarationUnit.symbolName);
            break;
        case "class":
            root.rootDeclarationUnitIds.push(declarationUnit.id);
            root.instanceContainerSymbols.add(declarationUnit.symbolName);
            root.staticContainerSymbols.add(declarationUnit.symbolName);
            break;
        case "interface":
            root.instanceContainerSymbols.add(declarationUnit.symbolName);
            break;
        default:
            break;
    }
}

/**
 * @param {SurfaceInventory} inventory
 * @param {string} libFileName
 * @param {Set<string>} selectedUnitIds
 * @param {Map<string, string>} unitTextOverrides
 * @param {Set<string>} completeContainerUnitIds
 * @param {Set<string>} excludedUnitIds
 * @returns {string | undefined}
 */
function emitFileSelection(inventory, libFileName, selectedUnitIds, unitTextOverrides, completeContainerUnitIds, excludedUnitIds) {
    const fileRecord = inventory.fileByLibFileName.get(libFileName);
    if (!fileRecord) {
        return undefined;
    }
    const fileUnits = inventory.unitsByLibFileName.get(libFileName) ?? [];
    if (!fileUnits.some(unit => selectedUnitIds.has(unit.id))) {
        return undefined;
    }

    if (fileRecord.preserveWholeFile) {
        // Whole-file preservation skips unit-level selection, so passing
        // through a file that contains excluded units would leak non-Baseline
        // surface wholesale. Selection can't fix that state, so fail closed.
        const excludedInFile = fileUnits
            .filter(unit => excludedUnitIds.has(unit.id))
            .map(unit => unit.id)
            .sort(compareStringsCaseSensitive);
        if (excludedInFile.length) {
            throw new Error(
                `Cannot emit whole-file lib ${libFileName}: it contains units excluded from the baseline target:\n${excludedInFile
                    .map(unitId => `- ${unitId}`)
                    .join("\n")}`,
            );
        }
        return [
            `/////////////////////////////`,
            `// ${libFileName}`,
            `/////////////////////////////`,
            stripSourceFilePreamble(fileRecord.sourceText).trimEnd(),
        ].join("\n");
    }

    const topLevelUnits = fileUnits.filter(unit => !unit.parentContainerId);
    /** @type {string[]} */
    const sections = [];

    for (const unit of topLevelUnits) {
        const emitted = emitUnit(inventory, unit.id, selectedUnitIds, unitTextOverrides, completeContainerUnitIds, excludedUnitIds);
        if (emitted) {
            sections.push(emitted.trimEnd());
        }
    }

    if (!sections.length) {
        return undefined;
    }

    return [
        `/////////////////////////////`,
        `// ${libFileName}`,
        `/////////////////////////////`,
        sections.join("\n\n"),
    ].join("\n");
}

/**
 * @param {SurfaceInventory} inventory
 * @param {string} unitId
 * @param {Set<string>} selectedUnitIds
 * @param {Map<string, string>} unitTextOverrides
 * @param {Set<string>} completeContainerUnitIds
 * @param {Set<string>} excludedUnitIds
 * @param {boolean} [forceEmit]
 * @returns {string | undefined}
 */
function emitUnit(inventory, unitId, selectedUnitIds, unitTextOverrides, completeContainerUnitIds, excludedUnitIds, forceEmit = false) {
    // Never emit an excluded unit, even through complete-container forceEmit.
    // The generator's invariant already rejects any intersection with the
    // selection set, so this acts as a mechanical last-resort guard.
    if (excludedUnitIds.has(unitId)) {
        return undefined;
    }
    const unit = inventory.unitById.get(unitId);
    if (!unit) {
        return undefined;
    }

    const childUnits = unit.containerId ? getChildUnits(inventory, unit.containerId) : [];
    /** @type {string[]} */
    const selectedChildEmissions = [];
    const forceChildren = forceEmit || completeContainerUnitIds.has(unit.id);
    for (const child of childUnits) {
        const childEmission = emitUnit(
            inventory,
            child.id,
            selectedUnitIds,
            unitTextOverrides,
            completeContainerUnitIds,
            excludedUnitIds,
            forceChildren,
        );
        if (childEmission) {
            selectedChildEmissions.push(childEmission);
        }
    }
    const isSelected = forceEmit || selectedUnitIds.has(unitId);
    const hasSelectedChildren = selectedChildEmissions.length > 0;

    if (!isSelected && !hasSelectedChildren) {
        return undefined;
    }

    if (unit.unitKind === "declaration" && unit.containerId) {
        const container = inventory.containerById.get(unit.containerId);
        if (!container) {
            throw new Error(`Missing container ${unit.containerId} for unit ${unit.id}`);
        }
        if (!selectedChildEmissions.length) {
            return `${container.headerText}\n${container.footerText}`.trimEnd();
        }
        return `${container.headerText}\n${indentBlock(selectedChildEmissions.join("\n\n"))}\n${container.footerText}`.trimEnd();
    }

    if (unit.unitKind === "declaration" && !unit.containerId) {
        return stripSourceFilePreamble(unitTextOverrides.get(unit.id) ?? unit.text).trimEnd();
    }

    return (unitTextOverrides.get(unit.id) ?? unit.text).trimEnd();
}

/**
 * @param {SurfaceInventory} inventory
 * @param {string} containerId
 */
function getChildUnits(inventory, containerId) {
    return inventory.units
        .filter(unit => unit.parentContainerId === containerId)
        .sort(compareUnitsByOrder);
}

