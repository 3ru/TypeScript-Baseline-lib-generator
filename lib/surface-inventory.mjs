/**
 * @param {string} fileName
 */
function isBuiltinSourceLibFileName(fileName) {
    if (!fileName.endsWith(".d.ts")) {
        return false;
    }
    if (!fileName.startsWith("lib.")) {
        return false;
    }
    const libName = fileName.slice("lib.".length, -".d.ts".length);

    if (libName === "baseline") {
        return false;
    }
    if (libName.endsWith(".full")) {
        return false;
    }
    if (libName === "esnext.disposable") {
        return false;
    }
    if (libName === "es6") {
        return false;
    }
    if (libName.startsWith("dom") || libName.startsWith("webworker") || libName === "scripthost") {
        return false;
    }
    if (libName === "decorators" || libName === "decorators.legacy") {
        return false;
    }

    return libName === "es5"
        || /^es\d{4}(?:\..+)?$/u.test(libName)
        || /^esnext(?:\..+)?$/u.test(libName);
}

/**
 * @param {string} libFileName
 */
function getLibStageRank(libFileName) {
    const libName = libFileName.replace(/^lib\./u, "").replace(/\.d\.ts$/u, "");
    if (libName === "es5") {
        return 5;
    }
    if (libName.startsWith("esnext")) {
        return Number.POSITIVE_INFINITY;
    }
    const match = /^es(\d{4})(?:\.|$)/u.exec(libName);
    if (match) {
        return Number.parseInt(match[1], 10);
    }
    return Number.POSITIVE_INFINITY;
}

/**
 * @param {string} filePath
 */
async function hashFile(filePath) {
    return `sha256-${createHash("sha256").update(await readFile(filePath)).digest("hex")}`;
}

/**
 * @param {{
 *   units: Array<InventoryUnitRecord & { node: ts.Node; sourceFile: ts.SourceFile; }>;
 *   containers: InventoryContainerRecord[];
 *   fileRecord: InventoryFileRecord;
 *   sourceFile: ts.SourceFile;
 *   statement: ts.Statement;
 *   containerPath: string[];
 *   parentContainerId: string | undefined;
 *   nextOrder: () => number;
 * }} options
 */
function collectStatementUnits(options) {
    const {
        units,
        containers,
        fileRecord,
        sourceFile,
        statement,
        containerPath,
        parentContainerId,
        nextOrder,
    } = options;

    if (ts.isModuleDeclaration(statement) && ts.isIdentifier(statement.name) && statement.body && ts.isModuleBlock(statement.body)) {
        const symbolName = formatSymbolName(containerPath, statement.name.text);
        const container = createContainerRecord({
            fileRecord,
            sourceFile,
            node: statement,
            bodyStart: statement.body.statements.pos,
            bodyEnd: statement.body.statements.end,
            containerKind: "namespace",
            symbolName,
            parentContainerId,
            nextOrder,
        });
        containers.push(container);
        fileRecord.containerIds.push(container.id);

        units.push(createDeclarationUnit({
            fileRecord,
            sourceFile,
            node: statement,
            containerPath,
            parentContainerId,
            nextOrder,
            symbolName,
            declarationKind: "namespace",
            typeRefName: undefined,
            containerId: container.id,
        }));

        for (const childStatement of statement.body.statements) {
            collectStatementUnits({
                units,
                containers,
                fileRecord,
                sourceFile,
                statement: childStatement,
                containerPath: [...containerPath, statement.name.text],
                parentContainerId: container.id,
                nextOrder,
            });
        }
        return;
    }

    if (ts.isInterfaceDeclaration(statement) && ts.isIdentifier(statement.name)) {
        const symbolName = formatSymbolName(containerPath, statement.name.text);
        const container = createContainerRecord({
            fileRecord,
            sourceFile,
            node: statement,
            bodyStart: statement.members.pos,
            bodyEnd: statement.members.end,
            containerKind: "interface",
            symbolName,
            parentContainerId,
            nextOrder,
        });
        containers.push(container);
        fileRecord.containerIds.push(container.id);

        units.push(createDeclarationUnit({
            fileRecord,
            sourceFile,
            node: statement,
            containerPath,
            parentContainerId,
            nextOrder,
            symbolName,
            declarationKind: "interface",
            typeRefName: undefined,
            containerId: container.id,
        }));

        for (const member of statement.members) {
            const memberUnit = createMemberUnit({
                fileRecord,
                sourceFile,
                node: member,
                ownerSymbol: symbolName,
                containerPath: [...containerPath, statement.name.text],
                parentContainerId: container.id,
                nextOrder,
            });
            if (memberUnit) {
                units.push(memberUnit);
            }
        }
        return;
    }

    if (ts.isClassDeclaration(statement) && statement.name && ts.isIdentifier(statement.name)) {
        const symbolName = formatSymbolName(containerPath, statement.name.text);
        const container = createContainerRecord({
            fileRecord,
            sourceFile,
            node: statement,
            bodyStart: statement.members.pos,
            bodyEnd: statement.members.end,
            containerKind: "class",
            symbolName,
            parentContainerId,
            nextOrder,
        });
        containers.push(container);
        fileRecord.containerIds.push(container.id);

        units.push(createDeclarationUnit({
            fileRecord,
            sourceFile,
            node: statement,
            containerPath,
            parentContainerId,
            nextOrder,
            symbolName,
            declarationKind: "class",
            typeRefName: undefined,
            containerId: container.id,
        }));

        for (const member of statement.members) {
            const memberUnit = createMemberUnit({
                fileRecord,
                sourceFile,
                node: member,
                ownerSymbol: symbolName,
                containerPath: [...containerPath, statement.name.text],
                parentContainerId: container.id,
                nextOrder,
            });
            if (memberUnit) {
                units.push(memberUnit);
            }
        }
        return;
    }

    if (ts.isVariableStatement(statement)) {
        const declaration = statement.declarationList.declarations[0];
        if (declaration?.type && ts.isTypeLiteralNode(declaration.type) && ts.isIdentifier(declaration.name)) {
            const symbolName = formatSymbolName(containerPath, declaration.name.text);
            const container = createContainerRecord({
                fileRecord,
                sourceFile,
                node: statement,
                bodyStart: declaration.type.members.pos,
                bodyEnd: declaration.type.members.end,
                containerKind: "type-literal-var",
                symbolName,
                parentContainerId,
                nextOrder,
            });
            containers.push(container);
            fileRecord.containerIds.push(container.id);

            units.push(createDeclarationUnit({
                fileRecord,
                sourceFile,
                node: statement,
                containerPath,
                parentContainerId,
                nextOrder,
                symbolName,
                declarationKind: "var",
                typeRefName: symbolName,
                containerId: container.id,
            }));

            for (const member of declaration.type.members) {
                const memberUnit = createMemberUnit({
                    fileRecord,
                    sourceFile,
                    node: member,
                    ownerSymbol: symbolName,
                    containerPath: [...containerPath, declaration.name.text],
                    parentContainerId: container.id,
                    nextOrder,
                });
                if (memberUnit) {
                    units.push(memberUnit);
                }
            }
            return;
        }
    }

    units.push(createDeclarationUnit({
        fileRecord,
        sourceFile,
        node: statement,
        containerPath,
        parentContainerId,
        nextOrder,
        symbolName: getStatementSymbolName(statement, containerPath),
        declarationKind: getDeclarationKind(statement),
        typeRefName: getTypeReferenceName(statement, containerPath),
        containerId: undefined,
    }));
}

/**
 * @param {{
 *   fileRecord: InventoryFileRecord;
 *   sourceFile: ts.SourceFile;
 *   node: ts.Node;
 *   bodyStart: number;
 *   bodyEnd: number;
 *   containerKind: "namespace" | "interface" | "class" | "type-literal-var";
 *   symbolName: string;
 *   parentContainerId: string | undefined;
 *   nextOrder: () => number;
 * }} options
 * @returns {InventoryContainerRecord & { node: ts.Node; sourceFile: ts.SourceFile; bodyStart: number; bodyEnd: number; }}
 */
function createContainerRecord(options) {
    const {
        fileRecord,
        sourceFile,
        node,
        bodyStart,
        bodyEnd,
        containerKind,
        symbolName,
        parentContainerId,
        nextOrder,
    } = options;
    return {
        id: `${fileRecord.libFileName}::container::${containerKind}::${symbolName}::${nextOrder()}`,
        libFileName: fileRecord.libFileName,
        containerKind,
        symbolName,
        parentContainerId,
        headerText: stripSourceFilePreamble(getNodeSlice(fileRecord.sourceText, node.getFullStart(), bodyStart)).replace(/^(?:\r?\n)+/, "").trimEnd(),
        footerText: getNodeSlice(fileRecord.sourceText, bodyEnd, node.end).trimStart(),
        order: fileRecord.unitIds.length + fileRecord.containerIds.length,
        bodyStart,
        bodyEnd,
        node,
        sourceFile,
    };
}

/**
 * @param {{
 *   fileRecord: InventoryFileRecord;
 *   sourceFile: ts.SourceFile;
 *   node: ts.Node;
 *   containerPath: string[];
 *   parentContainerId: string | undefined;
 *   nextOrder: () => number;
 *   symbolName: string | undefined;
 *   declarationKind: DeclarationKind;
 *   typeRefName: string | undefined;
 *   containerId: string | undefined;
 * }} options
 * @returns {InventoryUnitRecord & { node: ts.Node; sourceFile: ts.SourceFile; }}
 */
function createDeclarationUnit(options) {
    const {
        fileRecord,
        sourceFile,
        node,
        containerPath,
        parentContainerId,
        nextOrder,
        symbolName,
        declarationKind,
        typeRefName,
        containerId,
    } = options;

    /** @type {InventoryUnitRecord & { node: ts.Node; sourceFile: ts.SourceFile; }} */
    const unit = {
        id: createUnitId(fileRecord.libFileName, symbolName ?? "anonymous", nextOrder()),
        libFileName: fileRecord.libFileName,
        unitKind: "declaration",
        declarationKind,
        containerPath,
        parentContainerId,
        containerId,
        symbolName,
        ownerSymbol: undefined,
        memberName: undefined,
        typeRefName,
        declaredSymbols: symbolName ? [symbolName] : [],
        dependencySymbols: [],
        dependencyUnitIds: [],
        order: fileRecord.unitIds.length,
        text: getNodeText(fileRecord.sourceText, node),
        parameterNames: getParameterNames(node),
        surfacePath: symbolName,
        node,
        sourceFile,
    };
    fileRecord.unitIds.push(unit.id);
    return unit;
}

