/**
 * The caller (generator) resolves the lib directory through the fail-closed
 * verification in lib/toolchain-libs.mjs. This only enumerates the directory
 * contents. reportPath is the canonical path written to checked-in reports; it
 * keeps the real path of the platform-specific package
 * (node_modules/@typescript/typescript-<os>-<arch>/...) out of the artifact
 * (the real path is environment-dependent and breaks determinism).
 *
 * @param {{
 *   libDirectory: string;
 *   reportPathPrefix: string;
 * }} options
 */
export async function discoverBuiltinSourceLibEntries(options) {
    const libDirectory = options.libDirectory;
    const entries = await readdir(libDirectory, { withFileTypes: true });
    const sourceFiles = entries
        .filter(entry => entry.isFile())
        .map(entry => entry.name)
        .filter(fileName => isBuiltinSourceLibFileName(fileName))
        .sort(compareStringsCaseSensitive);

    /** @type {BuiltinSourceLibEntry[]} */
    const sourceLibEntries = [];
    for (const sourceFileName of sourceFiles) {
        const sourcePath = path.join(libDirectory, sourceFileName);
        sourceLibEntries.push({
            sourceFileName,
            libFileName: sourceFileName,
            sourcePath,
            reportPath: `${options.reportPathPrefix}/${sourceFileName}`,
            sourceHash: await hashFile(sourcePath),
        });
    }

    return sourceLibEntries;
}

/**
 * @param {{
 *   snapshotName: string;
 *   repoRoot: string;
 *   sourceLibEntries: BuiltinSourceLibEntry[];
 *   inventoryOutputPath: string;
 * }} options
 */
export async function createSurfaceInventory(options) {
    const {
        snapshotName,
        repoRoot,
        sourceLibEntries,
        inventoryOutputPath,
    } = options;

    /** @type {InventoryFileRecord[]} */
    const files = [];
    /** @type {InventoryContainerRecord[]} */
    const containers = [];
    /** @type {Array<InventoryUnitRecord & { node: ts.Node; sourceFile: ts.SourceFile; }>} */
    const units = [];

    for (const sourceLibEntry of sourceLibEntries) {
        const sourceText = await readFile(sourceLibEntry.sourcePath, "utf8");
        const sourceFile = ts.createSourceFile(
            sourceLibEntry.sourceFileName,
            sourceText,
            ts.ScriptTarget.Latest,
            /*setParentNodes*/ true,
            ts.ScriptKind.TS,
        );
        const fileRecord = {
            sourceFileName: sourceLibEntry.sourceFileName,
            libFileName: sourceLibEntry.libFileName,
            sourcePath: sourceLibEntry.sourcePath,
            reportPath: sourceLibEntry.reportPath,
            sourceHash: sourceLibEntry.sourceHash,
            referenceLibs: getReferenceLibs(sourceText),
            preserveWholeFile: shouldPreserveWholeFile(sourceFile, sourceText),
            stageRank: getLibStageRank(sourceLibEntry.libFileName),
            sourceText,
            unitIds: [],
            containerIds: [],
        };
        files.push(fileRecord);

        let order = 0;
        for (const statement of sourceFile.statements) {
            collectStatementUnits({
                units,
                containers,
                fileRecord,
                sourceFile,
                statement,
                containerPath: [],
                parentContainerId: undefined,
                nextOrder: () => order++,
            });
        }
    }

    const fileByLibFileName = new Map(files.map(fileRecord => [fileRecord.libFileName, fileRecord]));
    const containersByIdForWrite = new Map(containers.map(container => [container.id, stripRuntimeFields(container)]));

    /** @type {Map<string, InventoryUnitRecord[]>} */
    const declarationUnitsBySymbol = new Map();
    /** @type {Map<string, InventoryUnitRecord[]>} */
    const rootDeclarationUnitsByCompatName = new Map();
    /** @type {Map<string, InventoryUnitRecord[]>} */
    const memberUnitsByOwnerAndName = new Map();
    /** @type {Map<string, InventoryUnitRecord[]>} */
    const memberUnitsByOwnerSymbol = new Map();
    /** @type {Map<string, RootSurfaceRecord>} */
    const rootSurfaceByCompatName = new Map();
    /** @type {Map<string, string[]>} */
    const declarationUnitIdsBySymbol = new Map();
    /** @type {Map<string, InventoryUnitRecord[]>} */
    const unitsByLibFileName = new Map();

    // Pass 1: build just the symbol index dependency resolution needs.
    for (const unit of units) {
        if (unit.unitKind === "declaration" && unit.symbolName) {
            let declarationUnitIds = declarationUnitIdsBySymbol.get(unit.symbolName);
            if (!declarationUnitIds) {
                declarationUnitIds = [];
                declarationUnitIdsBySymbol.set(unit.symbolName, declarationUnitIds);
            }
            declarationUnitIds.push(unit.id);
        }
    }

    // Pass 2: finalize the dependency fields on the original unit.
    for (const unit of units) {
        unit.dependencySymbols = collectDependencySymbols({
            unit,
            declarationUnitIdsBySymbol,
        });
        unit.dependencyUnitIds = [
            ...new Set(unit.dependencySymbols.flatMap(symbol => declarationUnitIdsBySymbol.get(symbol) ?? [])),
        ].filter(unitId => unitId !== unit.id).sort(compareStringsCaseSensitive);
    }

    // Pass 3: make one stripped copy per unit and share the same object across
    // unitById and every lookup map. A separate copy per map splits into copies
    // that do and don't carry later-computed fields (dependency info, etc.),
    // producing a bug where the classification result changes with the map you
    // read from (a real failure that once dead-coded inherited-member
    // classification).
    /** @type {Map<string, InventoryUnitRecord>} */
    const strippedUnitById = new Map(units.map(unit => [unit.id, stripRuntimeFields(unit)]));

    for (const unit of units) {
        const strippedUnit = strippedUnitById.get(unit.id);
        if (!strippedUnit) {
            throw new Error(`Missing stripped unit for ${unit.id}`);
        }

        let fileUnits = unitsByLibFileName.get(unit.libFileName);
        if (!fileUnits) {
            fileUnits = [];
            unitsByLibFileName.set(unit.libFileName, fileUnits);
        }
        fileUnits.push(strippedUnit);

        if (unit.unitKind === "declaration" && unit.symbolName) {
            let symbolUnits = declarationUnitsBySymbol.get(unit.symbolName);
            if (!symbolUnits) {
                symbolUnits = [];
                declarationUnitsBySymbol.set(unit.symbolName, symbolUnits);
            }
            symbolUnits.push(strippedUnit);

            if (!unit.symbolName.endsWith("Constructor")) {
                let rootUnits = rootDeclarationUnitsByCompatName.get(unit.symbolName);
                if (!rootUnits) {
                    rootUnits = [];
                    rootDeclarationUnitsByCompatName.set(unit.symbolName, rootUnits);
                }
                rootUnits.push(strippedUnit);
            }
        }

        if (unit.unitKind === "member" && unit.ownerSymbol && unit.memberName) {
            const memberKey = getMemberLookupKey(unit.ownerSymbol, unit.memberName);
            let memberUnits = memberUnitsByOwnerAndName.get(memberKey);
            if (!memberUnits) {
                memberUnits = [];
                memberUnitsByOwnerAndName.set(memberKey, memberUnits);
            }
            memberUnits.push(strippedUnit);

            let ownerUnits = memberUnitsByOwnerSymbol.get(unit.ownerSymbol);
            if (!ownerUnits) {
                ownerUnits = [];
                memberUnitsByOwnerSymbol.set(unit.ownerSymbol, ownerUnits);
            }
            ownerUnits.push(strippedUnit);
        }
    }

    for (const rootDeclarationUnits of rootDeclarationUnitsByCompatName.values()) {
        rootDeclarationUnits.sort(compareUnitsByOrder);
    }
    for (const memberUnits of memberUnitsByOwnerAndName.values()) {
        memberUnits.sort(compareUnitsByOrder);
    }
    for (const ownerUnits of memberUnitsByOwnerSymbol.values()) {
        ownerUnits.sort(compareUnitsByOrder);
    }
    for (const declarationUnits of declarationUnitsBySymbol.values()) {
        declarationUnits.sort(compareUnitsByOrder);
    }
    for (const fileUnits of unitsByLibFileName.values()) {
        fileUnits.sort(compareUnitsByOrder);
    }

    for (const declarationUnits of declarationUnitsBySymbol.values()) {
        for (const declarationUnit of declarationUnits) {
            registerRootSurface(rootSurfaceByCompatName, declarationUnit);
        }
    }
    for (const rootSurface of rootSurfaceByCompatName.values()) {
        const readonlySymbol = READONLY_COMPANION_BY_RUNTIME_ROOT.get(rootSurface.compatName);
        if (!readonlySymbol) {
            continue;
        }
        const hasRuntimeRoot = rootSurface.rootDeclarationUnitIds.some(unitId => {
            const declarationKind = strippedUnitById.get(unitId)?.declarationKind;
            return declarationKind === "var" || declarationKind === "class";
        });
        if (!hasRuntimeRoot || !declarationUnitsBySymbol.has(readonlySymbol)) {
            throw new Error(`Missing readonly companion ${readonlySymbol} for ${rootSurface.compatName}`);
        }
        const rootMemberNames = new Set(
            (memberUnitsByOwnerSymbol.get(rootSurface.compatName) ?? [])
                .flatMap(unit => unit.memberName ? [unit.memberName] : []),
        );
        const readonlyMemberNames = new Set(
            (memberUnitsByOwnerSymbol.get(readonlySymbol) ?? [])
                .flatMap(unit => unit.memberName ? [unit.memberName] : []),
        );
        const unmatchedMembers = [...readonlyMemberNames]
            .filter(memberName => !rootMemberNames.has(memberName))
            .sort(compareStringsCaseSensitive);
        if (!readonlyMemberNames.size || unmatchedMembers.length) {
            throw new Error(
                `Readonly companion ${readonlySymbol} does not structurally match ${rootSurface.compatName}: `
                    + (unmatchedMembers.length ? unmatchedMembers.join(", ") : "no members"),
            );
        }
        rootSurface.instanceContainerSymbols.add(readonlySymbol);
    }

    const inventory = {
        snapshot: snapshotName,
        inventoryOutputPath,
        files,
        containers: containers.map(container => stripRuntimeFields(container)),
        fileByLibFileName,
        containerById: containersByIdForWrite,
        declarationUnitsBySymbol,
        rootDeclarationUnitsByCompatName,
        memberUnitsByOwnerAndName,
        memberUnitsByOwnerSymbol,
        rootSurfaceByCompatName,
        units: units.map(unit => {
            const strippedUnit = strippedUnitById.get(unit.id);
            if (!strippedUnit) {
                throw new Error(`Missing stripped unit for ${unit.id}`);
            }
            return strippedUnit;
        }),
        unitById: strippedUnitById,
        unitsByLibFileName,
    };

    await mkdir(path.dirname(inventoryOutputPath), { recursive: true });
    await writeFile(
        inventoryOutputPath,
        `${JSON.stringify(getInventoryReport(inventory, repoRoot), undefined, 2)}\n`,
    );

    return inventory;
}

/**
 * @param {{
 *   inventory: SurfaceInventory;
 *   selectedUnitIds: Iterable<string>;
 *   unitTextOverrides?: Map<string, string>;
 *   completeContainerUnitIds?: Set<string>;
 *   excludedUnitIds?: Set<string>;
 * }} options
 */
export function emitSelectedUnits(options) {
    const {
        inventory,
        selectedUnitIds,
        unitTextOverrides = new Map(),
        completeContainerUnitIds = new Set(),
        excludedUnitIds = new Set(),
    } = options;
    const selectedSet = new Set(selectedUnitIds);
    /** @type {string[]} */
    const fileSections = [];
    for (const fileRecord of inventory.files) {
        const fileSection = emitFileSelection(
            inventory,
            fileRecord.libFileName,
            selectedSet,
            unitTextOverrides,
            completeContainerUnitIds,
            excludedUnitIds,
        );
        if (fileSection) {
            fileSections.push(fileSection);
        }
    }

    return [
        GENERATED_LIB_HEADER,
        fileSections.join("\n\n").trimEnd(),
        "",
    ].join("\n");
}

/**
 * @param {string} compatKey
 */
export function getCompatSegments(compatKey) {
    if (compatKey.startsWith("javascript.builtins.")) {
        return compatKey.slice("javascript.builtins.".length).split(".");
    }
    const argumentsPrefix = "javascript.functions.arguments";
    if (compatKey === argumentsPrefix) {
        return ["IArguments"];
    }
    if (compatKey.startsWith(`${argumentsPrefix}.`)) {
        return ["IArguments", ...compatKey.slice(argumentsPrefix.length + 1).split(".")];
    }
    throw new Error(`Unsupported JavaScript lib compat key ${compatKey}`);
}

/**
 * @param {string} compatKey
 */
export function isLibCompatKey(compatKey) {
    return compatKey.startsWith("javascript.builtins.")
        || compatKey === "javascript.functions.arguments"
        || compatKey.startsWith("javascript.functions.arguments.");
}

/**
 * @param {SurfaceInventory} inventory
 * @param {string[]} compatSegments
 */
export function findLongestCompatRoot(inventory, compatSegments) {
    for (let length = compatSegments.length; length >= 1; length--) {
        const candidate = compatSegments.slice(0, length).join(".");
        if (inventory.rootSurfaceByCompatName.has(candidate)) {
            return candidate;
        }
    }
    return undefined;
}

